#1. Compara dos valores numericos dentro de una variable que sea ingresadas por el usuario.
# Ambas de tipo entero y con un print comprueba si son iguales (==)
num1 = 2
num2 = 2
print(num1 == num2) #La salida sera un valor buleano Verdadero o Falso (true ; false)

#2. Compara dos valores numericos dentro de una variable que sea ingresadas por el usuario.
# Ambas de tipo entero y con un print comprueba su desigualdad (!=)
num1 = 2
num2 = 2
print(num1 != num2) #La salida sera un valor buleano Verdadero o Falso (true ; false)

